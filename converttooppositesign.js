// write a program that converts given number into opposite sign( +ve into -ve and vise versa)

function convertToOppositeSign(number) {
    num = -number
    return num;
}

console.log(convertToOppositeSign(10));  // Output: -10
console.log(convertToOppositeSign(-1587655)); // Output: 15
console.log(convertToOppositeSign(0));   // Output: 0
